# Utilize um loop while e um loop for para contar de 0 até o número que o usuário digitar:

# ------------------------------------------ ESCREVA SEU CÓDIGO ABAIXO -----------------------------------------------------------

# LOOP WHILE
num1 = int(input('Digite um número: '))

controle = 0

while controle <= num1:
    
    print(controle)

    controle += 1




# LOOP FOR

numero = int(input("Digite um número: "))

for i in range(numero + 1):
    print(i)
